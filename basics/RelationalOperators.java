package basics;

/**
 * Relational Operator
 * >, <, >=, <=, ==, !=
 * returns true/false
 */
public class RelationalOperators {
    public static void main(String[] args) {
        System.out.println(1 > 3);   // false
        System.out.println(2.5 < 2.9);   // true
        System.out.println(10 >= 20);   // false
        System.out.println(30 <= 30);   // true
        System.out.println(3.4 == 34);   // false
        System.out.println(19 != 23);   // true
    }
}
